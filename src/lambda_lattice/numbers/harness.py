def demo_block(demos, pos, neg):
    lines = [f"Every number below belongs to the same hidden category of "
             f"numbers between 1 and 100. Numbers in the category are labeled "
             f"'{pos}'; numbers outside it are labeled '{neg}'."]
    for d in demos:
        lines.append(f"Number: {d} -> {pos}")
    return "\n".join(lines)


def probe_prompt(block, probe, pos, neg):
    if INSTRUCT == "sizep":
        return (f"{block}\n\nNumber: {probe}\n"
                f"First list every rule that is consistent with ALL the "
                f"labeled numbers above. Then apply the size principle: every "
                f"additional labeled number that is consistent with a narrow "
                f"rule makes it a suspicious coincidence unless that narrow "
                f"rule is the true one — so the MORE labeled numbers you have "
                f"seen, the more strongly you should trust only the narrowest "
                f"consistent rule and reject numbers outside it; with only a "
                f"FEW numbers, broader consistent rules remain plausible and "
                f"you should be more permissive. Count the labeled numbers "
                f"above and weigh this explicitly. Then answer '{pos}' ONLY "
                f"IF this number is accepted under that reasoning; otherwise "
                f"answer '{neg}'. End your reply with exactly 'Answer: {pos}' "
                f"or 'Answer: {neg}'.")
    if INSTRUCT == "meet":
        return (f"{block}\n\nNumber: {probe}\n"
                f"First list every rule that is consistent with ALL the "
                f"labeled numbers above. Then answer '{pos}' ONLY IF this "
                f"number satisfies EVERY one of those rules; otherwise answer "
                f"'{neg}'. End your reply with exactly 'Answer: {pos}' or "
                f"'Answer: {neg}'.")
    return (f"{block}\n\nNumber: {probe}\n"
            f"Decide the label of this number. End your reply with exactly "
            f"'Answer: {pos}' or 'Answer: {neg}'.")


def strip_think(text: str) -> tuple[str, int]:
    """Return (final segment, think chars). Handles Qwen3 <think>, R1, gpt-oss."""
    if "</think>" in text:
        head, _, tail = text.rpartition("</think>")
        return tail, len(head)
    if "assistantfinal" in text:
        head, _, tail = text.rpartition("assistantfinal")
        return tail, len(head)
    if "<|channel|>final<|message|>" in text:
        head, _, tail = text.rpartition("<|channel|>final<|message|>")
        return tail, len(head)
    return text, 0


def parse_label(final_seg: str, pos: str, neg: str):
    """Prefer last 'Answer: X'; fallback last occurrence of either label."""
    m = list(re.finditer(rf"answer\s*[:\-]?\s*\**\s*({pos}|{neg})",
                         final_seg.lower()))
    if m:
        return int(m[-1].group(1) == pos)
    hits = [(mm.start(), lab) for lab in (pos, neg)
            for mm in re.finditer(re.escape(lab), final_seg.lower())]
    if not hits:
        return None
    return int(max(hits)[1] == pos)



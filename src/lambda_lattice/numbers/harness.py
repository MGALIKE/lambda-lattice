def render_chat(tok, user_msg: str, mode: str) -> str:
    kw = {}
    if mode in ("on", "off"):
        kw["enable_thinking"] = mode == "on"
    elif mode in ("high", "low"):
        kw["reasoning_effort"] = mode
    return tok.apply_chat_template(
        [{"role": "user", "content": user_msg}],
        tokenize=False, add_generation_prompt=True, **kw)


class VllmBackend:
    def __init__(self, name: str):
        from transformers import AutoTokenizer
        from vllm import LLM

        self.tok = AutoTokenizer.from_pretrained(name)
        self.llm = LLM(model=name, dtype="bfloat16", max_model_len=MAXLEN,
                       gpu_memory_utilization=0.92, enforce_eager=False)

    def generate(self, user_msgs, seeds, mode):
        from vllm import SamplingParams

        prompts = [render_chat(self.tok, m, mode) for m in user_msgs]
        params = [SamplingParams(temperature=TEMP, top_p=TOP_P,
                                 max_tokens=MAXNEW, seed=s) for s in seeds]
        outs = self.llm.generate(prompts, params)
        res = []
        for o in outs:
            c = o.outputs[0]
            res.append((c.text, c.finish_reason == "length"))
        return res


class HfBackend:
    def __init__(self, name: str):
        import torch
        from transformers import AutoModelForCausalLM, AutoTokenizer

        self.torch = torch
        self.tok = AutoTokenizer.from_pretrained(name)
        if self.tok.pad_token is None:
            self.tok.pad_token = self.tok.eos_token
        self.tok.padding_side = "left"
        self.model = AutoModelForCausalLM.from_pretrained(
            name, dtype=torch.bfloat16, device_map="auto").eval()

    def generate(self, user_msgs, seeds, mode):
        res = []
        for i in range(0, len(user_msgs), BATCH):
            chunk = [render_chat(self.tok, m, mode)
                     for m in user_msgs[i:i + BATCH]]
            self.torch.manual_seed(seeds[i])
            enc = self.tok(chunk, return_tensors="pt", padding=True,
                           add_special_tokens=False).to(self.model.device)
            with self.torch.no_grad():
                out = self.model.generate(
                    **enc, max_new_tokens=MAXNEW, do_sample=True,
                    temperature=TEMP, top_p=TOP_P,
                    pad_token_id=self.tok.pad_token_id)
            n_in = enc["input_ids"].shape[1]
            for row in out:
                text = self.tok.decode(row[n_in:], skip_special_tokens=True)
                res.append((text, len(row) - n_in >= MAXNEW))
        return res


class MockBackend:
    """Instrument power check (no GPU): 'bayes' = sampling oracle from the
    exact size-principle predictive; 'prox' = sampling oracle from numeric
    proximity (scale 6, deliberately != the reference's 10 to show the stats
    are robust to scale mismatch). Both re-parse demos/probe from the PROMPT
    TEXT, so the check also covers prompt readability end to end."""

    def __init__(self, name: str):
        assert name in ("bayes", "prox"), name
        self.kind = name

    def generate(self, user_msgs, seeds, mode):
        res = []
        for msg, sd in zip(user_msgs, seeds):
            lab = re.search(r"labeled\s+'(\w+)'; numbers outside it are "
                            r"labeled\s+'(\w+)'", msg)
            pos, neg = lab.group(1), lab.group(2)
            demos = [int(x) for x in
                     re.findall(rf"Number: (\d+) -> {pos}\b", msg)]
            probe = int(re.findall(r"Number: (\d+)", msg)[-1])
            if self.kind == "bayes":
                p = bayes_predict(demos, [probe])[probe]
            else:
                p = math.exp(-min(abs(probe - d) for d in demos) / 6.0)
            acc = random.Random(sd).random() < p
            res.append((f"Answer: {pos if acc else neg}", False))
        return res



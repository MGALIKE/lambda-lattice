def trial_cells():
    rng = range(SEED0, SEED0 + SEEDS)
    cells = []
    if "a" in ARMS:
        cells += [("a", 8, s) for s in rng]
    if "b" in ARMS:
        cells += [("b", nd, s) for nd in NDEMOS_B for s in rng]
    return cells


DISC_KINDS = ("in_far", "off_near", "broad_only", "out_far")
LAM_KINDS = ("off_near", "broad_only")


def run_condition(be, name: str, mode: str) -> dict:
    cells = trial_cells()
    prompts, seeds, meta = [], [], []
    trials = {}
    for arm, nd, seed in cells:
        pos, neg = LABELS if seed % 2 == 0 else (LABELS[1], LABELS[0])
        demos, probes, rname = make_ng_trial(nd, seed, big=(arm == "a"))
        bay = bayes_predict(demos, [v for v, _ in probes])
        block = demo_block(demos, pos, neg)
        key = (arm, nd, seed)
        trials[key] = {"pos": pos, "neg": neg, "demos": demos, "rule": rname,
                       "probes": probes, "bayes": bay, "rows": []}
        for idx, (val, kind) in enumerate(probes):
            prompts.append(probe_prompt(block, val, pos, neg))
            seeds.append(3_000_000 + (0 if arm == "a" else 1_000_000)
                         + nd * 4_000 + seed * 100 + idx)
            meta.append((key, idx, kind))

    print(f"[{name}:{mode}] {len(prompts)} prompts ...", flush=True)
    t0 = time.time()
    outs = be.generate(prompts, seeds, mode)
    gen_s = time.time() - t0
    print(f"[{name}:{mode}] generation done in {gen_s:.0f}s", flush=True)

    thinks = mode in ("on", "native", "high", "low")
    traces = []
    for (key, idx, kind), (text, truncated) in zip(meta, outs):
        tr = trials[key]
        final_seg, think_chars = strip_think(text)
        if truncated and thinks and think_chars == 0:
            ans = None
        elif truncated and not final_seg.strip():
            ans = None
        else:
            ans = parse_label(final_seg, tr["pos"], tr["neg"])
        val = tr["probes"][idx][0]
        tr["rows"].append({"kind": kind, "val": val, "ans": ans,
                           "bayes": tr["bayes"][val],
                           "prox": prox_predict(tr["demos"], val),
                           "think_chars": think_chars, "trunc": int(truncated)})
        if len(traces) < 20 and kind in LAM_KINDS:
            traces.append({"key": str(key), "kind": kind, "val": val,
                           "think": text[:2000], "final": final_seg[:300]})

    trial_rows = []
    for (arm, nd, seed), tr in trials.items():
        rows = tr["rows"]

        def rate(kinds, answered=True):
            v = [r["ans"] for r in rows if r["kind"] in kinds
                 and r["ans"] is not None]
            return sum(v) / len(v) if v else float("nan")

        def ref(field, kinds):
            v = [r[field] for r in rows if r["kind"] in kinds]
            return sum(v) / len(v) if v else float("nan")

        rep = [r["ans"] for r in rows if r["kind"] == "repeat"
               and r["ans"] is not None]
        outp = [r["ans"] for r in rows if r["kind"] == "out_far"
                and r["ans"] is not None]
        sanity = ((sum(rep) + sum(1 - a for a in outp))
                  / max(len(rep) + len(outp), 1))
        n_ans = sum(1 for r in rows if r["ans"] is not None)
        disc = [r for r in rows if r["kind"] in DISC_KINDS
                and r["ans"] is not None]
        fit_b = (sum(1 - abs(r["ans"] - r["bayes"]) for r in disc)
                 / len(disc)) if disc else float("nan")
        fit_p = (sum(1 - abs(r["ans"] - r["prox"]) for r in disc)
                 / len(disc)) if disc else float("nan")
        trial_rows.append({
            "arm": arm, "n": nd, "seed": seed, "rule": tr["rule"],
            "lambda": rate(LAM_KINDS),
            "lambda_in": rate(("in_far",)),
            "lambda_off": rate(("off_near",)),
            "lambda_broad": rate(("broad_only",)),
            "out_acc": rate(("out_far",)),
            "sanity": sanity, "parse_rate": n_ans / len(rows),
            "trunc_rate": sum(r["trunc"] for r in rows) / len(rows),
            "think_chars": sum(r["think_chars"] for r in rows) / len(rows),
            "bayes": ref("bayes", LAM_KINDS),        # scorer-compat: ref on λ
            "bayes_in": ref("bayes", ("in_far",)),
            "bayes_off": ref("bayes", ("off_near",)),
            "bayes_broad": ref("bayes", ("broad_only",)),
            "bayes_out": ref("bayes", ("out_far",)),
            "prox": ref("prox", LAM_KINDS),
            "prox_in": ref("prox", ("in_far",)),
            "fit_bayes": fit_b, "fit_prox": fit_p,
            "detail": [(r["kind"], r["val"], r["ans"],
                        round(r["bayes"], 4), round(r["prox"], 4))
                       for r in rows]})
    return {"mode": mode, "fmt": FMT, "gen_s": round(gen_s, 1),
            "sampling": {"temp": TEMP, "top_p": TOP_P, "max_new": MAXNEW},
            "instruct": INSTRUCT, "trials": trial_rows, "traces": traces}


def summarize(cond: dict):
    tr = [t for t in cond["trials"] if t["sanity"] >= 0.75
          and t["parse_rate"] >= 0.8]
    out = {"n_learned": len(tr), "n_total": len(cond["trials"])}

    def m(ts, k):
        v = [t[k] for t in ts if t[k] == t[k]]
        return round(sum(v) / len(v), 3) if v else None

    a = [t for t in tr if t["arm"] == "a"]
    if a:
        for k in ("lambda", "lambda_in", "lambda_off", "lambda_broad",
                  "out_acc", "fit_bayes", "fit_prox", "bayes", "bayes_in",
                  "prox", "prox_in"):
            out[f"armA_{k}"] = m(a, k)
        if out["armA_fit_bayes"] is not None and out["armA_fit_prox"] is not None:
            out["armA_delta_fit"] = round(
                out["armA_fit_bayes"] - out["armA_fit_prox"], 3)
    for nd in NDEMOS_B:
        b = [t for t in tr if t["arm"] == "b" and t["n"] == nd]
        if b:
            out[f"armB_lambda_n{nd}"] = m(b, "lambda")
            out[f"armB_in_n{nd}"] = m(b, "lambda_in")
            out[f"armB_bayes_n{nd}"] = m(b, "bayes")
            out[f"armB_prox_n{nd}"] = m(b, "prox")
    # paired slope lambda(nmax)-lambda(nmin), and the Bayes reference slope
    lo, hi = NDEMOS_B[0], NDEMOS_B[-1]
    for field, label in (("lambda", "slope"), ("bayes", "bayes_slope"),
                         ("lambda_in", "slope_in")):
        d_lo = {t["seed"]: t[field] for t in tr
                if t["arm"] == "b" and t["n"] == lo and t[field] == t[field]}
        d_hi = {t["seed"]: t[field] for t in tr
                if t["arm"] == "b" and t["n"] == hi and t[field] == t[field]}
        common = sorted(set(d_lo) & set(d_hi))
        if len(common) >= 2:
            d = [d_hi[s] - d_lo[s] for s in common]
            mu = sum(d) / len(d)
            var = sum((x - mu) ** 2 for x in d) / (len(d) - 1)
            se = (var / len(d)) ** 0.5
            out[f"armB_{label}"] = round(mu, 3)
            out[f"armB_{label}_se"] = round(se, 3)
            out[f"armB_{label}_z"] = round(mu / max(se, 1e-9), 2)
    out["mean_think_chars"] = round(
        sum(t["think_chars"] for t in cond["trials"])
        / max(len(cond["trials"]), 1), 1)
    return out


def _run_all() -> dict:
    OUT.parent.mkdir(parents=True, exist_ok=True)
    all_res = {}
    if OUT.exists():
        try:
            all_res = json.loads(OUT.read_text(encoding="utf-8"))
        except Exception:
            all_res = {}
    by_model: dict[str, list[str]] = {}
    for spec in MODELS:
        name, _, mode = spec.rpartition(":")
        if not name:
            name, mode = spec, "none"
        by_model.setdefault(name, []).append(mode)
    for name, modes in by_model.items():
        be = (MockBackend if BACKEND == "mock"
              else VllmBackend if BACKEND == "vllm" else HfBackend)(name)
        for mode in modes:
            cond_key = f"{name}:{mode}"
            print(f"==== {cond_key} ====", flush=True)
            cond = run_condition(be, name, mode)
            cond["summary"] = summarize(cond)
            all_res[cond_key] = cond
            all_res["_meta"] = {"seeds": SEEDS, "seed0": SEED0, "arms": ARMS,
                                "fmt": FMT, "ndemos_b": NDEMOS_B,
                                "backend": BACKEND, "instruct": INSTRUCT,
                                "task": "numgame",
                                "rule_share": RULE_SHARE,
                                "erlang_sigma": ERLANG_SIGMA,
                                "prox_scale": PROX_SCALE}
            OUT.write_text(json.dumps(all_res), encoding="utf-8")
            print(json.dumps(cond["summary"], indent=1), flush=True)
            print(f"wrote {OUT}", flush=True)
        del be
    return all_res


def _as_list(v):
    return v if isinstance(v, list) else str(v).split(",")


def run(config: dict | None = None) -> dict:
    """Python API entry point. Config keys mirror the ETH_* env vars without the
    prefix (lowercase): models, seeds, seed0, arms, fmt, maxnew, maxlen, backend,
    batch, ndemos_b, instruct, out. Returns the merged results dict (written to
    disk). Set backend="mock" with models="bayes:none,prox:none" for the no-GPU
    power check."""
    config = config or {}
    global MODELS, SEEDS, SEED0, ARMS, FMT, MAXNEW, MAXLEN, BACKEND, BATCH

"""numbers/harness.py — Tenenbaum NUMBER-GAME port of the lambda instrument,
answering the single-task-family objection.

Adapted (imports/paths/packaging only) from the pre-registered
``frontier_lab/echo_numgame.py``. Numeric logic unchanged; the hypothesis space,
priors, and exact learners are imported from ``lambda_lattice.references``.

Same measurement logic as the reasoning harness (generation lambda, thinking
toggle, ""|meet|sizep instruction branches, sanity/parse gates, per-trial exact
references) on Tenenbaum's (2000) number game over [1,100]:

  demos  n positive examples of a hidden numeric category, generated by one of
         10 demo rules (multiples/last-digit/primes), each with a designated
         BROADER superset rule in H
  probes stratified to separate rule membership from numeric proximity:
    in_far      in narrow rule, held out, >=8 away from every possible demo
    off_near    demo+-1, outside both rules
    broad_only  in the broad rule only, >=3 from every demo
    out_far     outside both rules, >=8 from every demo (both accounts: reject)
    repeat      demo re-membership sanity (must accept)

  lambda := acceptance on off_near + broad_only (generalization beyond the
  narrowest consistent rule). Bayes reference contracts lambda(n) -> 0;
  a similarity/proximity learner keeps it flat.

Env knobs: SAME ETH_* names as the reasoning harness.
  ETH_MODELS, ETH_SEEDS, ETH_SEED0, ETH_ARMS, ETH_FMT (ng1), ETH_MAXNEW,
  ETH_MAXLEN, ETH_BACKEND vllm|hf|mock, ETH_BATCH, ETH_OUT
  (default echo_numgame.json), ETH_NDEMOS_B, ETH_INSTRUCT ""|meet|sizep

No-GPU power check (mock oracles must be recovered by the stats), via CLI:
  ETH_BACKEND=mock ETH_MODELS=bayes:none,prox:none ETH_SEEDS=36 \
  ETH_OUT=echo_numgame_mockcheck.json python -m lambda_lattice.numbers.harness
or via the python API:
  numbers.harness.run({"models": "bayes:none,prox:none", "backend": "mock",
                       "seeds": 36, "out": "..."} )
"""

GAP_IN = 8   # min distance from any in_far probe to any possible demo
GAP_OUT = 8  # min distance from any out_far probe to every sampled demo
GAP_BROAD = 3


# ---------------------------------------------------------------------------
# demo rules: (name, designated broad superset rule in H, 4 held-out in_far
# probes). Demo pool = rule members <= min(probes) - GAP_IN (primes: 2 also
# dropped so demos stay inside the broad rule 'odd').
# ---------------------------------------------------------------------------
DEMO_RULES = [
    ("mult8", "mult4", (72, 80, 88, 96)),
    ("mult6", "mult3", (78, 84, 90, 96)),
    ("mult9", "mult3", (72, 81, 90, 99)),
    ("mult4", "even", (88, 92, 96, 100)),
    ("end0", "mult5", (70, 80, 90, 100)),
    ("end5", "mult5", (65, 75, 85, 95)),
    ("end3", "odd", (63, 73, 83, 93)),
    ("end7", "odd", (67, 77, 87, 97)),
    ("end4", "even", (64, 74, 84, 94)),
    ("primes", "odd", (79, 83, 89, 97)),
]


def demo_pool(rname: str, probe_in) -> list[int]:
    members = sorted(RULES_EXT[rname])
    pool = [m for m in members if m <= min(probe_in) - GAP_IN]
    if rname == "primes":
        pool = [m for m in pool if m != 2]
    return pool


for _r, _b, _p in DEMO_RULES:  # design invariants
    # probes are held-out members of the narrow rule; the demo POOL (not the
    # whole rule: primes includes 2, odd doesn't) must lie inside the broad
    # rule so 'broad' is always a consistent broader hypothesis
    assert set(_p) <= RULES_EXT[_r]
    assert set(demo_pool(_r, _p)) <= RULES_EXT[_b]
    assert len(demo_pool(_r, _p)) >= 4 and len(_p) == 4


def make_ng_trial(nd: int, seed: int, big: bool):
    """One trial: demos (nested across nd for paired slopes) + stratified
    probes. Returns (demos, probes[(value, kind)], rule_name)."""
    ridx = seed % len(DEMO_RULES)
    rname, bname, probe_in_all = DEMO_RULES[ridx]
    rule, broad = RULES_EXT[rname], RULES_EXT[bname]
    pool = demo_pool(rname, probe_in_all)
    # demo rng independent of nd -> demo multisets nested across the n-sweep
    drng = random.Random(50_000 + 1_000_000 * ridx + seed)
    order = pool[:]
    drng.shuffle(order)
    if nd <= len(order):
        demos = order[:nd]
    else:  # repetition when nd exceeds distinct pool, like the Boolean harness
        demos = order + [drng.choice(pool) for _ in range(nd - len(order))]
    demos = demos[:]
    drng.shuffle(demos)
    dset = set(demos)

    # probe rng independent of nd where possible (in_far identical across n)
    prng = random.Random(70_000 + 1_000_000 * ridx + seed)
    q = 4 if big else 2
    in_far = list(probe_in_all) if big else prng.sample(probe_in_all, 2)
    off_cand = sorted({d + e for d in dset for e in (-1, 1)}
                      - rule - broad - dset)
    off_cand = [x for x in off_cand if 1 <= x <= NMAX]
    off_near = prng.sample(off_cand, min(q, len(off_cand)))
    broad_cand = [x for x in sorted(broad - rule)
                  if min(abs(x - d) for d in dset) >= GAP_BROAD]
    broad_only = prng.sample(broad_cand, min(q, len(broad_cand)))
    out_cand = [x for x in range(1, NMAX + 1)
                if x not in rule and x not in broad
                and min(abs(x - d) for d in dset) >= GAP_OUT]
    n_out = 4 if big else 1
    out_far = prng.sample(out_cand, min(n_out, len(out_cand)))
    repeats = prng.sample(sorted(dset), min(2 if big else 1, len(dset)))

    probes = ([(v, "in_far") for v in in_far]
              + [(v, "off_near") for v in off_near]
              + [(v, "broad_only") for v in broad_only]
              + [(v, "out_far") for v in out_far]
              + [(v, "repeat") for v in repeats])
    return demos, probes, rname


